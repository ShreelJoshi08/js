// Problem Definition
// A transport company needs a system to manage different types of
// vehicles. Build a base class and extend it for cars and motorcycles.
// Key Questions to be evaluated during/after Implementation
// 1. How does inheritance enhance code reusability?
// 2. What are the differences between properties of subclasses?
// 3.. How can methods be overridden effectively?
// Supplementary Problems –
// • Add a method to calculate fuel efficiency
// • Include maintenance schedule functionality
// Key Skills to be addressed –
// • Object-oriented programming, inheritance, encapsulation
// Applications –
// • Fleet management software, insurance calculators
// Learning Outcome –
// Students will design scalable class-based structures using inheritance.


